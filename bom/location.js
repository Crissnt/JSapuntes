//Returns the current URL

location.href

//We can go to a URL with this method

location.href="https://google.es"
location="https://google.es"

//Returns the protocol name used to access the app

location.protocol

//Returns the host direction including port if it's not standard

location.host

//Returns the host direction, but without the port

location.hostname

//Returns the path from the host

location.pathname

//Returns the search string

location.search

//Returns the URL marker, if it exists

location.hash

//Returns the username in the URL, if it exists

location.username

//Returns the password in the URL, if it exists

location.password

//Returns the canonical address of the URL

location.origin

//Reloads the page. If the server parameter is specified, recharges the page from the server

location.reload([server])